            <h2>Tudo o que você precisa saber sobre UX e UI para alavancar seu ecommerce</h2>
            <p>Hoje, uma das prioridades dos empreendedores digitais é a experiencia do usuário. Para um ecommerce ter um bom posicionamento é imprescindível boas práticas de UX e UI. Mesmo sendo pouco conhecido, essas técnicas merecem muita atenção. </p>
            <br><br>
            <h2>O que é UX e UI para e-commerce?</h2>
            <p>Primeiramente é muito importante sabermos o conceito para depois tratarmos da sua importância. UX Design, é um termo da língua inglesa, USER EXPERIENCE, significa justamente experiência do usuário. O objetivo é tornar a usabilidade e acessibilidade do seu ecommerce, a melhor possível. É preciso estar sempre atento em como fazer com que o usuário permaneça mais tempo em sua loja virtual.
                A navegação do seu cliente não pode gerar dificuldades, ele tem que encontrar o que precisa e deve perceber as vantagens. Todo o trabalho não será percebido pelo usuário, ele simplesmente deve usufruir sem perceber o resultado, ou seja, a navegação deve ser intuitiva e natural.  
                As estratégias devem considerar a construção do layout, o desenvolvimento do conteúdo, e claro o comportamento do usuário, etc. 
                Enquanto o UX se preocupa em como a experiência do usuário pode ser a melhor possível, o UI cria uma interface com diversos elementos que proporcionam isso – botões, anúncios, banners, cores, fontes, estilos, imagens, etc. 
                Os conceitos de UX e UI em um ecommerce deve ser desenvolvido de forma paralela, porém integrados.
            </p>
            <img src="../img/blog/ux-e-ui-para-ecommerce.jpg" alt="" class="img-responsive">
            <br><br>
            <h2>Por que fazer UX e UI para seu ecommerce?</h2>
            <p>Para facilitar o entendimento, vamos pensar quando entramos em uma loja física procurando um produto e não encontra e ainda acaba sendo mal atendido. Qual a chance de voltar? Com certeza você irá embora sem comprar e ainda falará mal dessa loja para todos os amigos. Certo? Isso é o que devemos evitar em sua loja virtual. 
            Outro ponto muito importante é o excesso de informação e de recursos. Por medo disso muitas lojas pecam no excesso de atenção e isso acaba causando uma situação desconfortável para o usuário, fazendo também que não volte mais e não compre em sua loja. Quando for traçar o planejamento de UX e UI, pare uns minutos e pense: <strong>Como eu gostaria de ser tratado em uma loja fisica e como eu quero que uma loja virtual seja quando for visitá-la?</strong>
            </p>
            <h2>Foco na Conversão</h2>
            <p>
                Uma das vantagens do UX para o e-commerce é mobilizar o usuário para a compra, com foco justamente no que o produto tem a oferecer. É preciso atenção em como um determinado item está representado na loja e as opções de interação que o site oferece. A ideia é promover mais agilidade e menos dificuldade para o usuário.
            </p>
            <p>
                O UX permite uma navegação clara e dinâmica, sem excesso de cliques, retornos ruins ou problemas de carregamento. O projeto de UX pode, ainda, ter foco no que deseja o cliente, apresentando, de maneira estratégica, mais daquilo que ele busca. Quando o gestor do e-commerce sabe sobre os desejos e emoções do cliente, pode desenvolver uma estrutura de navegação mais assertiva.
            </p>
            <p>
                O UI entra nesta fase: além da funcionalidade (UX), a identidade visual é muito importante para impactar o cliente. São conceitos que devem trabalhar juntos. Quanto ao UI, é preciso criar um projeto em que os elementos devem dialogar. São eles:
            </p>
            <ul>
                <li>Escolha das cores</li>
                <li>Estilo da loja</li>
                <li>Organização das informações</li>
                <li>Harmonização das páginas</li>
                <li>Layout</li>
                <li>Imagens de alta qualidade.</li>
            </ul>
            <br><br>
            <h2>Como aplicar UX e UI para e-commerce?</h2>
            <p>Antes de saber como aplicar essa técnica é imprescindível responder as questões abaixo:</p>
            <ul>
                <li>Quem é?</li>
                <li>O que busca?</li>
                <li>Quais produtos secundários buscaria?</li>
                <li>Quando acessa?</li>
                <li>Como acessa?</li>
                <li>Quanto tempo permanece no e-commerce?</li>
                <li>O que o faz efetivar a compra?</li>
                <li>Quais páginas menos e mais acessadas?</li>
                <li>O comportamento do usuário.</li>
            </ul>
            <br><br>
            <h2>Em seguida, é preciso conhecer a estrutura do seu e-commerce atual: </h2>
            <ul>
                <li>O que funciona</li>
                <li>Erros das páginas</li>
                <li>Problemas quanto às imagens e conteúdo</li>
                <li>O esqueleto de páginas e funções de menu</li>
                <li>Elementos visuais de forma geral</li>
            </ul>
            <p>
                É preciso reconhecer problemas e pontos a melhorar.
                Há muitas ferramentas e boas práticas para implantar o UX e UI para e-commerce, mas o ponto de partida é saber as necessidades do seu cliente e da sua própria loja. Além de uma implementação total, é sempre importante considerar estes conceitos durante toda a sua atuação, já que um site sempre tem mudanças e novidades.
            </p>
